void main() {
  int a = 23;
  int b = 10;

  int add = a + b;
  int sub = a - b;
  int mul = a * b;
  double div = a / b;
  int mod = a % b;

  print('a=$a, b=$b');
  print('Addition: $a + $b = $add');
  print('Subtraction: $a - $b = $sub');
  print('Multiplication: $a * $b = $mul');
  print('Division: $a / $b = $div');
  print('Modulus: $a % $b = $mod');
}
